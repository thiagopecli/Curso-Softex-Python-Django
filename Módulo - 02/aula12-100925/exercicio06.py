"""6.  Cálculo de Média: Crie uma função que receba três notas e retorne a média delas. Imprima o resultado."""

def calcular_media(nota1, nota2, nota3):
    return (nota1 + nota2 + nota3) / 3

resultado = calcular_media(7, 8, 9)
print(f"A média é: {resultado}")
resultado = calcular_media(5, 6, 7)
print(f"A média é: {resultado}")