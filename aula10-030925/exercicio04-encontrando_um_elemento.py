"""4.  Encontrando um Elemento: 
Crie a tupla nomes = ('Carlos', 'Ana', 'Pedro'). Use o método 
.index() para encontrar o índice do nome 'Ana'."""

nomes = ("Carlos", "Ana", "Pedro")
print(f"Tuplas de nomes: {nomes}")

indice_ana = nomes.index("Ana")
print(f"O nome de 'Ana' está no índice: {indice_ana}")