"""5.  Área de um Retângulo: Crie uma função que receba a base e a altura de um retângulo e 
retorne sua área. Imprima o resultado."""

def area_retangulo(base, altura):
    return base * altura

resultado = area_retangulo(5, 10)
print(f"A área do retângulo é: {resultado}")
resultado = area_retangulo(7, 3)
print(f"A área do retângulo é: {resultado}")