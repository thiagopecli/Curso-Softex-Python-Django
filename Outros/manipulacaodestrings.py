# 8. Manipulação de Strings:

# Dada a string "programação".
#   Inverta a string
#   Verifique se a string original é igual a string invertida:

palavra = "programação"
palavra_invertida = palavra[::-1] # Invertendo a string

print(palavra_invertida) # Imprimindo a palavra invertida
print(palavra == palavra_invertida)