# 2. Operações com string:

# Dada a string "Python é incrivel", faça o seguinte:
#   Conte quantos caracteres ela possui(incluindo espaços);
#   Converta toda a string para maiúsculas;
#   Substitua a palavra "incrivel" por "poderoso".

palavra = "Python é incrível!"

print(len(palavra)) # Contagem de caracteres.
print(palavra.upper()) # Convertendo toda string para maiúsculas.
print(palavra.replace("incrível", "poderoso")) # Substituindo a palavra "incrível" por "poderoso".