# 9. Listas Aninhadas: 

# Dada a lista matriz = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]:
#   Acesse e imprima o número 5.
#   Substitua o número 8 por 10. 

lista = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
print(lista[1][1]) # Acessando e imprimindo o número 5 da lista.

lista[2][1] = 10 # Substituindo o número 8 por 10.
print(lista)